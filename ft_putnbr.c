#include "includes/libft.h"

void	ft_putnbr(int n)
{
	int long	i;

	i = n;
	if (i < 0)
	{
		ft_putchar('-');
		i = -i;
	}
	if (i > 9)
	{
		ft_putnbr(i / 10);
		ft_putnbr(i % 10);
	}
	if (i <= 9)
		ft_putchar(i + '0');
}
