#include "includes/ft_printf.h"

void		ft_putnbr_max(intmax_t i)
{
	if (i == -9223372036854775807 - 1)
	{
		ft_putchar('9');
		i = 223372036854775808;
	}
	if (i < 0)
		i = -i;
	if (i > 9)
	{
		ft_putnbr_max(i / 10);
		ft_putnbr_max(i % 10);
	}
	if (i <= 9)
		ft_putchar(i + '0');
}
